-- GameCare Desk MySQL schema draft v0.2
-- Purpose: Stage 2 DTO/OpenAPI support baseline. This is not yet a Flyway migration.
-- Domain focus: Steam game product, game version, purchase method, customer service conversation and work order handling.

CREATE TABLE tenant (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  public_id CHAR(26) NOT NULL COMMENT '对外暴露的 ULID',
  code VARCHAR(40) NOT NULL COMMENT '租户编码，MVP 默认单租户',
  name VARCHAR(120) NOT NULL COMMENT '租户名称',
  status VARCHAR(32) NOT NULL COMMENT '租户状态，例如 ACTIVE、DISABLED',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_tenant_public_id (public_id),
  UNIQUE KEY uk_tenant_code (code)
) COMMENT='租户/平台隔离边界表';

CREATE TABLE app_user (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  public_id CHAR(26) NOT NULL COMMENT '对外暴露的用户 ULID',
  username VARCHAR(80) NOT NULL COMMENT '登录用户名或演示账号名',
  display_name VARCHAR(80) NOT NULL COMMENT '页面显示名称',
  user_type VARCHAR(32) NOT NULL COMMENT '用户类型：CONSUMER、STAFF、ADMIN、SUPER_ADMIN',
  status VARCHAR(32) NOT NULL COMMENT '用户状态：ACTIVE、DISABLED、CANCELLED',
  password_hash VARCHAR(255) NULL COMMENT '密码强哈希；第三方登录或演示用户可为空',
  version INT NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_user_public_id (tenant_id, public_id),
  UNIQUE KEY uk_user_username (tenant_id, username),
  KEY idx_user_tenant_status (tenant_id, status),
  CONSTRAINT fk_user_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT
) COMMENT='消费者、客服、管理员和超级管理员的统一用户表';

CREATE TABLE role (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  code VARCHAR(40) NOT NULL COMMENT '角色编码，例如 CUSTOMER_SERVICE、ADMIN',
  name VARCHAR(80) NOT NULL COMMENT '角色名称',
  version INT NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_role_code (tenant_id, code),
  CONSTRAINT fk_role_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT
) COMMENT='租户内角色定义表';

CREATE TABLE user_role (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  user_id BIGINT NOT NULL COMMENT '用户 ID',
  role_id BIGINT NOT NULL COMMENT '角色 ID',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  UNIQUE KEY uk_user_role (tenant_id, user_id, role_id),
  KEY idx_user_role_role (role_id),
  CONSTRAINT fk_user_role_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT,
  CONSTRAINT fk_user_role_user FOREIGN KEY (user_id) REFERENCES app_user(id) ON DELETE RESTRICT,
  CONSTRAINT fk_user_role_role FOREIGN KEY (role_id) REFERENCES role(id) ON DELETE RESTRICT
) COMMENT='用户与角色的多对多关系表';

CREATE TABLE module_permission (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  module_code VARCHAR(64) NOT NULL COMMENT '模块编码，例如 conversations、tickets、games',
  module_label VARCHAR(80) NOT NULL COMMENT '模块显示名称',
  permission_code VARCHAR(64) NOT NULL COMMENT '权限编码，例如 view、manage、approve',
  permission_label VARCHAR(80) NOT NULL COMMENT '权限显示名称',
  status VARCHAR(32) NOT NULL COMMENT '权限状态：ACTIVE、DISABLED',
  version INT NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_module_permission (tenant_id, module_code, permission_code),
  KEY idx_module_permission_module (tenant_id, module_code, status),
  CONSTRAINT fk_module_permission_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT
) COMMENT='租户内可授权模块和权限点配置表';

CREATE TABLE role_module_permission (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  role_id BIGINT NOT NULL COMMENT '角色 ID',
  module_permission_id BIGINT NOT NULL COMMENT '模块权限 ID',
  visible TINYINT(1) NOT NULL DEFAULT 1 COMMENT '该角色是否展示对应模块或功能',
  enabled TINYINT(1) NOT NULL DEFAULT 1 COMMENT '该角色是否启用对应操作权限',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_role_module_permission (tenant_id, role_id, module_permission_id),
  KEY idx_role_module_permission_permission (module_permission_id),
  CONSTRAINT fk_role_module_permission_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT,
  CONSTRAINT fk_role_module_permission_role FOREIGN KEY (role_id) REFERENCES role(id) ON DELETE RESTRICT,
  CONSTRAINT fk_role_module_permission_permission FOREIGN KEY (module_permission_id) REFERENCES module_permission(id) ON DELETE RESTRICT
) COMMENT='角色与模块权限的多对多授权关系表';

CREATE TABLE game_product (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  public_id CHAR(26) NOT NULL COMMENT '对外暴露的游戏 ULID',
  steam_app_id VARCHAR(40) NULL COMMENT 'Steam AppID；非 Steam 或演示数据可为空',
  title VARCHAR(160) NOT NULL COMMENT '游戏标题',
  subtitle VARCHAR(240) NOT NULL COMMENT '游戏副标题或营销短文案',
  description TEXT NULL COMMENT '游戏详情介绍',
  platform VARCHAR(40) NOT NULL COMMENT '主要平台，当前优先 Steam',
  publish_status VARCHAR(32) NOT NULL COMMENT '上架状态：DRAFT、LISTED、UNLISTED、ARCHIVED',
  cover_url VARCHAR(500) NULL COMMENT '封面图 URL，冗余保存便于列表快速展示',
  refund_rule TEXT NULL COMMENT '退款规则说明快照',
  version INT NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_game_public_id (tenant_id, public_id),
  UNIQUE KEY uk_game_steam_app_id (tenant_id, steam_app_id),
  KEY idx_game_status_updated (tenant_id, publish_status, updated_at),
  KEY idx_game_title (tenant_id, title),
  CONSTRAINT fk_game_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT
) COMMENT='Steam 游戏商品主表，表示一个游戏本体';

CREATE TABLE game_version (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  product_id BIGINT NOT NULL COMMENT '所属游戏商品 ID',
  public_id CHAR(26) NOT NULL COMMENT '对外暴露的游戏版本 ULID',
  version_code VARCHAR(64) NOT NULL COMMENT '版本编码，例如 STANDARD、DELUXE、ULTIMATE',
  version_title VARCHAR(160) NOT NULL COMMENT '版本名称，例如标准版、豪华版、年度版',
  description TEXT NULL COMMENT '版本说明，例如包含 DLC、皮肤或额外内容',
  status VARCHAR(32) NOT NULL COMMENT '版本状态：ACTIVE、DISABLED、ARCHIVED',
  sort_order INT NOT NULL DEFAULT 0 COMMENT '前端展示排序',
  version INT NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_game_version_public_id (tenant_id, public_id),
  UNIQUE KEY uk_game_version_code (product_id, version_code),
  KEY idx_game_version_product (tenant_id, product_id, status, sort_order),
  CONSTRAINT fk_game_version_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT,
  CONSTRAINT fk_game_version_product FOREIGN KEY (product_id) REFERENCES game_product(id) ON DELETE RESTRICT
) COMMENT='游戏版本表，例如标准版、豪华版、年度版或 DLC 合集';

CREATE TABLE purchase_method (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  code VARCHAR(64) NOT NULL COMMENT '购买方式编码，例如 CDK、STEAM_GIFT、ACCOUNT、OFFLINE_PLAY、DOWNLOAD',
  name VARCHAR(80) NOT NULL COMMENT '购买方式名称，例如 CDK、Steam 礼物、成品账号',
  description VARCHAR(500) NULL COMMENT '购买方式说明',
  default_delivery_handler VARCHAR(64) NOT NULL COMMENT '默认交付处理器，用于后端选择交付流程',
  requires_manual_review TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否默认需要人工审核或人工交付',
  status VARCHAR(32) NOT NULL COMMENT '购买方式状态：ACTIVE、DISABLED',
  sort_order INT NOT NULL DEFAULT 0 COMMENT '前端展示排序',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_purchase_method_code (tenant_id, code),
  KEY idx_purchase_method_status (tenant_id, status, sort_order),
  CONSTRAINT fk_purchase_method_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT
) COMMENT='购买方式字典表，支持后续扩展新的售卖/交付方式';

CREATE TABLE game_offer (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  product_id BIGINT NOT NULL COMMENT '冗余所属游戏商品 ID，便于查询和约束',
  version_id BIGINT NOT NULL COMMENT '游戏版本 ID',
  purchase_method_id BIGINT NOT NULL COMMENT '购买方式 ID',
  public_id CHAR(26) NOT NULL COMMENT '对外暴露的可售方案 ULID',
  offer_code VARCHAR(80) NOT NULL COMMENT '可售方案编码，例如 ELDEN_RING_STANDARD_CDK',
  title VARCHAR(160) NOT NULL COMMENT '可售方案展示标题',
  price_amount DECIMAL(19,2) NOT NULL COMMENT '当前售价金额',
  original_price_amount DECIMAL(19,2) NULL COMMENT '划线原价金额',
  currency CHAR(3) NOT NULL COMMENT '币种，例如 CNY、USD',
  stock_quantity INT NOT NULL DEFAULT 0 COMMENT '可售库存数量；虚拟/人工交付可用较大值或单独策略',
  reserved_quantity INT NOT NULL DEFAULT 0 COMMENT '已预占库存数量',
  delivery_handler VARCHAR(64) NOT NULL COMMENT '实际交付处理器，例如 CDK_AUTO、STEAM_GIFT_MANUAL、ACCOUNT_MANUAL',
  delivery_config JSON NULL COMMENT '交付配置 JSON，例如区域限制、人工交付提示、离线包说明',
  status VARCHAR(32) NOT NULL COMMENT '方案状态：ACTIVE、DISABLED、SOLD_OUT',
  version INT NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_game_offer_public_id (tenant_id, public_id),
  UNIQUE KEY uk_game_offer_code (tenant_id, offer_code),
  UNIQUE KEY uk_game_offer_method (version_id, purchase_method_id),
  KEY idx_game_offer_product (tenant_id, product_id, status),
  KEY idx_game_offer_version (tenant_id, version_id, status),
  KEY idx_game_offer_method (tenant_id, purchase_method_id, status),
  CONSTRAINT fk_game_offer_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT,
  CONSTRAINT fk_game_offer_product FOREIGN KEY (product_id) REFERENCES game_product(id) ON DELETE RESTRICT,
  CONSTRAINT fk_game_offer_version FOREIGN KEY (version_id) REFERENCES game_version(id) ON DELETE RESTRICT,
  CONSTRAINT fk_game_offer_purchase_method FOREIGN KEY (purchase_method_id) REFERENCES purchase_method(id) ON DELETE RESTRICT
) COMMENT='游戏可售方案表，连接游戏版本与购买方式，并承载价格、库存和交付策略';

CREATE TABLE upload_asset (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  public_id CHAR(26) NOT NULL COMMENT '对外暴露的上传资产 ULID',
  purpose VARCHAR(32) NOT NULL COMMENT '上传用途，例如 GAME_MEDIA、KNOWLEDGE_DOC',
  provider VARCHAR(32) NOT NULL COMMENT '存储服务商，例如 ALIYUN_OSS',
  bucket_name VARCHAR(120) NOT NULL COMMENT 'OSS Bucket 名称',
  object_key VARCHAR(500) NOT NULL COMMENT 'OSS Object Key',
  original_file_name VARCHAR(255) NOT NULL COMMENT '原始文件名',
  content_type VARCHAR(120) NOT NULL COMMENT '文件 MIME 类型',
  size_bytes BIGINT NOT NULL COMMENT '文件大小，单位字节',
  checksum VARCHAR(128) NULL COMMENT '文件校验值，例如 SHA256 或 ETag',
  status VARCHAR(32) NOT NULL COMMENT '上传状态：PENDING、COMPLETED、FAILED、DELETED',
  uploader_id BIGINT NOT NULL COMMENT '上传人用户 ID',
  completed_at DATETIME(3) NULL COMMENT '上传完成时间，UTC',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_upload_asset_public_id (tenant_id, public_id),
  UNIQUE KEY uk_upload_asset_object_key (tenant_id, provider, bucket_name, object_key),
  KEY idx_upload_asset_status (tenant_id, purpose, status, created_at),
  KEY idx_upload_asset_uploader (tenant_id, uploader_id, created_at),
  CONSTRAINT fk_upload_asset_tenant FOREIGN KEY (tenant_id) REFERENCES tenant(id) ON DELETE RESTRICT,
  CONSTRAINT fk_upload_asset_uploader FOREIGN KEY (uploader_id) REFERENCES app_user(id) ON DELETE RESTRICT
) COMMENT='上传资产表，记录阿里云 OSS 等对象存储中的文件事实';

