  paid_amount DECIMAL(19,2) NOT NULL COMMENT '支付金额',
  currency CHAR(3) NOT NULL COMMENT '币种',
  paid_at DATETIME(3) NULL COMMENT '支付成功时间，UTC',
  provider_payload JSON NULL COMMENT '支付渠道返回载荷快照，演示环境可为空',
  created_at DATETIME(3) NOT NULL COMMENT '创建时间，UTC',
  updated_at DATETIME(3) NOT NULL COMMENT '更新时间，UTC',
  UNIQUE KEY uk_payment_order (tenant_id, order_id),
  UNIQUE KEY uk_payment_no (tenant_id, payment_no),
  KEY idx_payment_status_time (tenant_id, status, updated_at),
