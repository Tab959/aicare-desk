CREATE TABLE payment_snapshot (
  id BIGINT PRIMARY KEY COMMENT '内部主键，无业务含义',
  tenant_id BIGINT NOT NULL COMMENT '所属租户 ID',
  order_id BIGINT NOT NULL COMMENT '订单 ID',
  payment_no VARCHAR(40) NOT NULL COMMENT '演示支付流水号',
  channel VARCHAR(32) NOT NULL COMMENT '支付渠道，例如 MOCK、ALIPAY、WECHAT',
